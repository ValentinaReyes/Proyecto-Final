    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        int rut = Integer.parseInt(txtRut.getText());
        boolean isCertificated = false;
        
        if (rBtnSi.isSelected()) {
            isCertificated = true;
        }
        
            if (!"".equals(nombre) && !"".equals(apellido) && !"".equals(rut)) {

                Tutor tutor = new Tutor (nombre,rut, apellido, isCertificated);
                Conexion conexion = new Conexion();
                Connection con = conexion.getConexion();

                try {
                    PreparedStatement ps = con.prepareStatement("select * from tutores where rut = "+rut+" limit 1;");
                    ResultSet rs = ps.executeQuery();


                    if(rs.next()){
                        ps = con.prepareStatement("update tutores set nombre=?, apellido=?, isCertificated=? where rut=?");

                        ps.setString(1, nombre);
                        ps.setString(2, apellido);
                        ps.setBoolean(3, isCertificated);
                        ps.setInt(4, rut);
                        int result = ps.executeUpdate();
                        if (result > 0) {
                            cargarLista();
                            cargarDatosTabla();
                            limpiar();
                            JOptionPane.showMessageDialog(null, "Registro ha sido actualizado");
                        }else{
                            JOptionPane.showMessageDialog(null, "Registro no se ha actualizado");
                        }
                    }else{
                        JOptionPane.showMessageDialog(null, "No se ha encontrado el tutor con el rut: "+rut);
                    }
                    con.close();
                } catch (SQLException ex) {
                    Logger.getLogger(VentanaTutor.class.getName()).log(Level.SEVERE, null, ex);
                } 
            }else {
                JOptionPane.showMessageDialog(null, "Debe completar todos los campos");
            }
    }//GEN-LAST:event_btnActualizarActionPerformed

