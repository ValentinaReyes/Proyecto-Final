    public void cargarDatosTabla(){
        
        String array[][] = null;
        
        if (listaTutores.size() > 0) {
            // Determina las dimensiones de la matriz de la tabla
            array = new String [listaTutores.size()][4];
            for (int i = 0; i < listaTutores.size(); i++) {
                
                array[i][0]= listaTutores.get(i).getNombre();
                array[i][1]= listaTutores.get(i).getApellido();
                array[i][2]= String.valueOf (listaTutores.get(i).getRut());
                array[i][3]= listaTutores.get(i).isCertificated() == true ? "Si" : "No";
                
                tblTutores.setModel(new javax.swing.table.DefaultTableModel(
                        array,
                        new String[]{
                            "Nombre","Apellido", "Rut","¿Certificado?"
                        }
                ));        
            }
        } else {
            tblTutores.setModel(new javax.swing.table.DefaultTableModel(
                    array,
                    new String[]{
                        "Nombre", "Apellidos", "Rut", "¿Certificado?"
                    }
            ));
        
        }
    
    }
    
    private void cargarLista(){
        Conexion conexion = new Conexion();
        Connection con = conexion.getConexion();
        try {
            PreparedStatement ps = con.prepareStatement("select * from tutores;");
            ResultSet rs = ps.executeQuery();
            Tutor tutor;
            listaTutores.clear();
            while(rs.next()){
                tutor = new Tutor(rs.getString(2), rs.getInt(1), rs.getString(3), rs.getBoolean(4));
                listaTutores.add(tutor);
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(VentanaTutor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
     
