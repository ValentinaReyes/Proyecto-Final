    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
       
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        int rut = Integer.parseInt(txtRut.getText());
        boolean isCertificated = false;
        
        //Si esta seleccionado Si isCertificated es igual a true de lo contrario sigue como false
        if (rBtnSi.isSelected()) {
            isCertificated = true;
        }
        
         if (!"".equals(nombre) && !"".equals(apellido) && !"".equals(rut)) {
            Tutor tutor = new Tutor (nombre,rut, apellido, isCertificated);
            Conexion conexion = new Conexion();
            Connection con = conexion.getConexion();
            try {
                PreparedStatement ps = con.prepareStatement("insert into tutores (Rut, Nombre, Apellido, isCertificated) values(?,?,?,?)");
                ps.setInt(1, tutor.getRut());
                ps.setString(2, tutor.getNombre());
                ps.setString(3, tutor.getApellido());
                ps.setBoolean(4, tutor.isCertificated());
                int result = ps.executeUpdate();
                if (result > 0) {
                    cargarLista();
                    cargarDatosTabla();
                    limpiar();
                    JOptionPane.showMessageDialog(null, "Registro ha sido creado exitosamente");
                }else{
                    JOptionPane.showMessageDialog(null, "Registro no se ha creado");
                }
                con.close();
            } catch (SQLException ex) {
                Logger.getLogger(VentanaTutor.class.getName()).log(Level.SEVERE, null, ex);
            }
         }
        else {
            JOptionPane.showMessageDialog(null, "Debe completar todos los campos");
        }
       
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        int rut;
        rut = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese el registro del tutor a eliminar"));
       
        Conexion conexion = new Conexion();
        Connection con = conexion.getConexion();

            try {
                PreparedStatement ps = con.prepareStatement("select * from tutores where rut = "+rut+" limit 1;");
                ResultSet rs = ps.executeQuery();


                if(rs.next()){

                    ps = (PreparedStatement) con.prepareStatement("DELETE FROM tutores WHERE Rut=?");
                    ps.setString(1, String.valueOf(rut));

                    int result = ps.executeUpdate();
                    if(result>0){
                        cargarLista();
                        cargarDatosTabla();  
                        JOptionPane.showMessageDialog(null, "Registro eliminado de manera exitosa");                            
                    }else {
                        JOptionPane.showMessageDialog(null, "No se ha podido eliminar el registro");
                    }
               }else{
                    JOptionPane.showMessageDialog(null, "No se ha encontrado el tutor con el rut: "+rut);
                }
                con.close();
            } catch (SQLException ex) {
                Logger.getLogger(VentanaTutor.class.getName()).log(Level.SEVERE, null, ex);
            }     
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       
        int rut;   
        rut = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese el rut del tutor a buscar"));
        
        Conexion conexion = new Conexion();
        Connection con = conexion.getConexion();
        
        try {
            PreparedStatement ps = con.prepareStatement("select * from tutores where rut = "+rut+" limit 1;");
            ResultSet rs = ps.executeQuery();
            Tutor tutor;
            
            if(rs.next()){
                tutor = new Tutor(rs.getString(2), rs.getInt(1), rs.getString(3), rs.getBoolean(4));
                txtRut.setText(""+tutor.getRut());
                txtNombre.setText(tutor.getNombre());
                txtApellido.setText(tutor.getApellido());
                if (tutor.isCertificated()==true) {
                    rBtnSi.setSelected(true);
                }else {
                    rBtnNo.setSelected(true);
                }
                JOptionPane.showMessageDialog(null, tutor.mostrarDatos());
            }else{
                JOptionPane.showMessageDialog(null, "No se ha encontrado el tutor con el rut: "+rut);
            }
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(VentanaTutor.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

